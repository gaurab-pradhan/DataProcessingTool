    class Task extends SwingWorker<Void, Void> {

        /**
         * Main task. Executed in background thread.
         */
        @Override
        public Void doInBackground() throws InterruptedException {
            Random random = new Random();
            int progress = 0;
            String consParam = PropertiesUtil.getConsParam();
            String dbPath = PropertiesUtil.getDatabaseFilePath();
            try {
//                String pythonScriptPathCons = "python etl-extravaganza/etl/etl.py --act cons --src " + consParam + " --path \"" + filePath + "\" --db \"" + dbPath + "\"";
                String pythonScriptPathCons = consParam + " \"" + filePath + "\" --db \"" + dbPath + "\"";
                System.out.println(pythonScriptPathCons);
                // create runtime to execute external command
                Runtime rt = Runtime.getRuntime();
                Process pr = rt.exec(pythonScriptPathCons);
                // retrieve output from python script
                BufferedReader bfr = new BufferedReader(new InputStreamReader(pr.getInputStream()));
                String line = "";
                while ((line = bfr.readLine()) != null) {
                    // display each output line form python script
                    System.out.println(line);
                    consLogs.append(line);
                    consLogs.append("\n");
                }
                System.out.println("done");
            } catch (IOException ex) {
                Logger.getLogger(ConsolidationFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            return null;
        }

        /**
         * Executed in event dispatching thread
         */
        @Override
        public void done() {
            Toolkit.getDefaultToolkit().beep();
            process.setEnabled(true);
            setCursor(null); // turn off the wait cursor
        }
    }
